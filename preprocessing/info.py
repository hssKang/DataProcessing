import pandas as pd
import sys

# Data Exploration
def data_exploration_info(file):
    data=pd.read_csv(file)
    print("---------------------------------------------------------------------------head---------------------------------------------------------------------------")
    print(data.head())
    print("\n----------------------------------------------------------------------Number of Na----------------------------------------------------------------------")
    print(data.isna().sum())
    print("\n-----------------------------------------------------------------------Infomation-----------------------------------------------------------------------")
    print(data.info())
    print("\n------------------------------------------------------------------------Describe------------------------------------------------------------------------")
    print(data.iloc[:,0:-1].describe())     # Excluded the target
    
if __name__ == '__main__':
    file = sys.argv[1]
    data_exploration_info(file)